public abstract class Quadrilateros{
	
	protected double base1;
	protected double base2;
	protected double altura1;
	protected double altura2;

	public Quadrilateros(double base1, double altura1, double base2, double altura2){
		this.base1 = base1;
		this.altura1 = altura1;
		this.base2 = base2;
		this.altura2 = altura2;
	}

}
