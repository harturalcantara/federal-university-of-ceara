package br.crateus.ufc.teste1;

public class Times {
	private String name;
	private int point;
	
	public Times(String name, int point) {
		this.name = name;
		this.point = point;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	
}
